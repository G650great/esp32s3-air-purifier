void oled_draw_char(int x, int y, char c) {
    if (c < 32 || c > 127) c = '?';
    const uint8_t *glyph = font6x8[(uint8_t)c - 32];
    for (int col = 0; col < 6; col++) {
        uint8_t bits = glyph[col];
        for (int row = 0; row < 8; row++) {
            if (bits & (1 << row)) {
                int px = x + col;
                int py = y + row;
                if (px >= 0 && px < OLED_WIDTH && py >= 0 && py < OLED_HEIGHT) {
                    oled_buffer[(py / 8) * OLED_WIDTH + px] |= (1 << (py % 8));
                }
            }
        }
    }
}

void oled_draw_string(int x, int y, const char *str) {
    while (*str) {
        oled_draw_char(x, y, *str++);
        x += 6;
    }
}

void oled_draw_progress_bar(int x, int y, int w, int h, int percent) {
    oled_draw_rect(x, y, w, h);
    int fill_w = (w - 2) * percent / 100;
    if (fill_w > 0) {
        oled_fill_rect(x + 1, y + 1, fill_w, h - 2);
    }
}

void oled_set_font_small(void) {
    current_font_size = 0;
}

void oled_set_font_large(void) {
    current_font_size = 1;
}
